    <?php

    /*
    Realiza un programa que use los operadores aritméticos y muestre los resultados
    */

    // Declaración de variables
    $a = rand(1,20);
    $b = rand(1,20);

    // Uso de los operadores aritméticos
    echo -$a . "<br>"; // Negación
    echo $a + $b . "<br>"; // Suma
    echo $a - $b . "<br>"; // Resta
    echo $a * $b . "<br>"; // Multiplicación
    echo $a / $b . "<br>"; // División
    echo $a % $b . "<br>"; // Resto
    echo $a ** $b . "<br>"; // Potencia

    ?>